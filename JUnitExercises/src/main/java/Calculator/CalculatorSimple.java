package Calculator;

public class CalculatorSimple {

    public int sum(int a, int b) {
        return a + b;
    }

    public int subtract(int a, int b) {
        return a - b;
    }

    public int multiply(int a, int b) {
        return a * b;
    }

    public double division(int a, int b) {
        return a / b;
    }
}
